    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        Funcionario funcionario = new Funcionario();
        funcionario.setNome(jTextField2.getText());
        funcionario.setCpf(jTextField7.getText());
        funcionario.setEndereco(jTextField3.getText());
        funcionario.setTelefone(jTextField4.getText());
        funcionario.setEmail(jTextField5.getText());
        funcionario.setSexo((String) SelecionaSexo.getSelectedItem());
        funcionario.setLogin(jTextField8.getText());
        funcionario.setSenha(jTextField9.getText());
        funcionario.setCargo((String) SelecionaCargo.getSelectedItem());
        try {
            funcionario.setDataNascimento(new SimpleDateFormat("dd/MM/yyyy").parse(jTextField6.getText()));
        } catch (ParseException e) {
            e.printStackTrace();
        }
        funcionario.setSalario(Double.parseDouble(jTextField9.getText()));
        funcionario.setDataAdmissao(new Date());


        funcionarioController.salvar(funcionario);

        jTextField2.setText("");
        jTextField7.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField8.setText("");
        jTextField9.setText("");
        jTextField6.setText("");
        SelecionaSexo.setSelectedIndex(0);
        SelecionaCargo.setSelectedIndex(0);
    }

